    // ── Mobile menu toggle ──────────────────────────────────────
    const toggleBtn = document.getElementById('mobile-toggle');
    const mobileMenu = document.getElementById('mobile-menu');
    const ham1 = document.getElementById('ham1');
    const ham2 = document.getElementById('ham2');
    const ham3 = document.getElementById('ham3');

    toggleBtn.addEventListener('click', () => {
      const isOpen = mobileMenu.classList.toggle('open');
      toggleBtn.setAttribute('aria-expanded', isOpen);
      // Animate hamburger → X
      if (isOpen) {
        ham1.style.cssText = 'transform:translateY(8px) rotate(45deg)';
        ham2.style.cssText = 'opacity:0';
        ham3.style.cssText = 'transform:translateY(-8px) rotate(-45deg)';
      } else {
        ham1.style.cssText = '';
        ham2.style.cssText = '';
        ham3.style.cssText = '';
      }
    });

    function closeMobileMenu() {
      mobileMenu.classList.remove('open');
      toggleBtn.setAttribute('aria-expanded', 'false');
      ham1.style.cssText = '';
      ham2.style.cssText = '';
      ham3.style.cssText = '';
    }

    // ── Sticky header shadow on scroll ─────────────────────────
    const header = document.getElementById('site-header');
    window.addEventListener('scroll', () => {
      header.classList.toggle('header-scrolled', window.scrollY > 20);
    }, { passive: true });

    // ── Scroll reveal animations ──────────────────────────────
    const revealObserver = new IntersectionObserver(entries => {
      entries.forEach(e => {
        if (e.isIntersecting) { e.target.classList.add('visible'); revealObserver.unobserve(e.target); }
      });
    }, { threshold: 0.1, rootMargin: '0px 0px -50px 0px' });

    function reveal(sel, stagger) {
      document.querySelectorAll(sel).forEach((el, i) => {
        el.classList.add('reveal');
        if (stagger) el.style.animationDelay = (i % 3) * 0.15 + 's';
        revealObserver.observe(el);
      });
    }

    reveal('.product-card', true);
    reveal('#predimstva .flex.gap-5', true);
    reveal('#otzyvi .bg-white.rounded-2xl', true);
    reveal('#dostavka .bg-brand-gray', true);
    reveal('#products > div > .text-center');
    reveal('#dostavka > div > .text-center');
    reveal('#predimstva > div > .text-center');
    reveal('#otzyvi > div > .text-center');
    reveal('#peleti > h3');
